#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	int		len1;
	int		len2;

	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	str = (char *)malloc(sizeof(char) * (len1 + len2 + 1));
	if (str == NULL)
		return (NULL);
	else
	{
		ft_strlcpy(str, s1, len1 + 1);
		ft_strlcat(str, s2, len1 + len2 + 1);
		return (str);
	}
}
